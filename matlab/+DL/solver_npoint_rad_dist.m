function k = solver_npoint_rad_dist(u)

if size(u,1) == 3
    u = u ./ u([3;3;3],:);
else
    u = [u; ones(1,size(u,2))];
end

d = sum(u(1:2,:).^2);
Q = [u;d]*[u;d]';
Q = Q / norm(Q,'fro');
sols = solve(Q(:));

% find best solution
res = inf;
for i = 1:length(sols)
    uk = [u(1:2,:); 1+sols(i)*d];
    s = svd(uk);
    if s(3) < res
        k = sols(i);
        res = s(3);
    end
end


